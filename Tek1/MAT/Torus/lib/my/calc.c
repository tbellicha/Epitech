int add(int nb1, int nb2)
{
    int res = nb1 + nb2;
    return (res);
}

int subs(int nb1, int nb2)
{
    int res = nb1 - nb2;
    return (res);
}

int multi(int nb1, int nb2)
{
    int res = nb1 * nb2;
    return (res);
}

int divi(int nb1, int nb2)
{
    int res = nb1 / nb2;
    return (res);
}

int modu(int nb1, int nb2)
{
    int res = nb1 % nb2;
    return (res);
}